    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int row = tableproduct.getSelectedRow();
        if (row > -1) {
            String value = tableproduct.getValueAt(row, 0).toString();

            Connection con;
            try {
                con = getConnection();
                String delete = "DELETE FROM PRODUCT WHERE PRODUCTID=?";
                PreparedStatement st = con.prepareStatement(delete);
                st.setString(1, value);
                boolean b = st.execute();
                if (b) {
                    JOptionPane.showMessageDialog(this, "Product Delete Successfully");
                }
                loadAll();
            } catch (HeadlessException | SQLException ex) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select valid row");
        }

    }//GEN-LAST:event_btndeleteActionPerformed

