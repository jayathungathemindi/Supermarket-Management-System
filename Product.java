    private void tableproductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableproductMouseClicked
        int row = tableproduct.getSelectedRow();
        if (row > -1) {
            String value = tableproduct.getValueAt(row, 0).toString();

            Connection con;
            try {
                con = getConnection();
                String select = "SELECT * FROM PRODUCT WHERE PRODUCTID=?";
                PreparedStatement st = con.prepareStatement(select);
                st.setString(1, value);
                ResultSet rs = st.executeQuery();

                if (rs.next()) {

                    txtproductid.setText(rs.getString("PRODUCTID"));
                    txtname.setText(rs.getString("NAME"));
                    txtdesc.setText(rs.getString("DESCRIPTION"));
                    txtquan.setText(rs.getString("QUANTITY"));
                    txtcode.setText(rs.getString("CODE"));
                    txtcpri.setText(rs.getString("COSTPRICE"));
                    txtrpri.setText(rs.getString("RETAILPRICE"));
                    txtreorder.setText(rs.getString("REORDER"));
                }
                con.close();

            } catch (SQLException ex) {
            }
        }
    }//GEN-LAST:event_tableproductMouseClicked

