        if (txtname.getText().equals("") || txtcpri.getText().equals("") || txtquan.getText().equals("") || txtreorder.getText().equals("")) {
            System.out.println("null");
            JOptionPane.showMessageDialog(this, "Sorry! please enter the details as asked!");
        } else {
            System.out.println("null function suceeded");
            Connection con;
            try {
                con = getConnection();
                String product = "INSERT INTO PRODUCT (NAME,DESCRIPTION,QUANTITY,CODE,COSTPRICE,RETAILPRICE,REORDER) values(?,?,?,?,?,?,?)";//inserting data
                String pname = txtname.getText();
                String description = txtdesc.getText();
                double quantity = Double.parseDouble(txtquan.getText());
                String code = txtcode.getText();
                double costprice = Double.parseDouble(txtcpri.getText());
                double retailprice = Double.parseDouble(txtrpri.getText());
                int reorder = Integer.parseInt(txtreorder.getText());

                PreparedStatement st = con.prepareStatement(product);
                //getting data from the interface
                st.setString(1, pname);
                st.setString(2, description);
                st.setDouble(3, quantity);
                st.setString(4, code);
                st.setDouble(5, costprice);
                st.setDouble(6, retailprice);
                st.setInt(7, reorder);
                int a = st.executeUpdate();
                if (a > 0) {
                    System.out.println("updated");
                    JOptionPane.showMessageDialog(this, "Record added");
                } else {
                    JOptionPane.showMessageDialog(this, "error");
                }
                txtproductid.setText("");
                txtname.setText("");
                txtdesc.setText("");
                txtquan.setText("");
                txtcode.setText("");
                txtcpri.setText("");
                txtrpri.setText("");
                txtreorder.setText("");
                con.close();
                
                loadAll();
            } catch (SQLException e) {
                System.out.println(e);
            }

        }
